async function seedVenues() {
  console.log('ðŸŒ± Starting venue seed...');

  let created = 0;
  let skipped = 0;

  for (const venue of NYC_VENUES) {
    try {
      const existing = await prisma.venue.findFirst({
        where: {
          name: venue.name,
          city: venue.city,
        },
      });

      if (existing) {
        console.log(`â­ï¸  Skipping existing venue: ${venue.name}`);
        skipped++;
        continue;
      }

      await prisma.venue.create({
        data: venue,
      });

      console.log(`âœ… Created venue: ${venue.name}`);
      created++;
    } catch (error) {
      console.error(`âŒ Error creating venue ${venue.name}:`, error);
    }
  }

  console.log(`\nâœ¨ Venue seed complete!`);
  console.log(`Created: ${created}`);
  console.log(`Skipped: ${skipped}`);
  console.log(`Total venues in seed: ${NYC_VENUES.length}`);
}

seedVenues()
  .catch((error) => {
    console.error('Seed failed:', error);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
